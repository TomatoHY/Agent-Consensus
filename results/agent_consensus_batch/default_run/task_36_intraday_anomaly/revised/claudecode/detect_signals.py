def get_daily_data(code, days=30):
    """获取日K线数据"""
    client = quotes.Quotes.factory(market='std')
    try:
        df = client.bars(symbol=code, frequency=9, offset=0)
        if len(df) == 0:
            return None

        df.index = pd.to_datetime(df.index)
        # 按日聚合
        df_daily = df.resample('D').agg({
            'open': 'first',
            'high': 'max',
            'low': 'min',
            'close': 'last',
            'volume': 'sum'
        }).dropna()

        return df_daily.tail(days)
    except Exception as e:
        return None

def detect_intraday_anomaly_from_daily(df):
    """
    使用日K线数据近似检测盘中异动：
    - 大振幅（high-low）/open > 8% 且收盘在上半区
    - 成交量放大
    """
    signals = []

    if len(df) < 6:
        return signals

    for i in range(5, len(df)):
        row = df.iloc[i]
        prev_rows = df.iloc[i-5:i]

        # 计算振幅
        if row['open'] > 0:
            amplitude = (row['high'] - row['low']) / row['open'] * 100

            # 计算收盘位置
            if row['high'] > row['low']:
                closing_pos = (row['close'] - row['low']) / (row['high'] - row['low']) * 100
            else:
                closing_pos = 100.0

            # 计算成交量比
            avg_volume = prev_rows['volume'].mean()
            if avg_volume > 0:
                volume_ratio = row['volume'] / avg_volume
            else:
                volume_ratio = 1.0

            # 判断急拉信号：大振幅 + 收盘在上区 + 成交量放大
            if amplitude > 8.0 and closing_pos > 60 and volume_ratio > 3.0:
                # 验证次日持续
                if i < len(df) - 1:
                    next_row = df.iloc[i+1]
                    continuation = next_row['close'] >= row['low']

                    signals.append({
                        'date': row.name.date(),
                        'type': '急拉',
                        'amplitude': amplitude,
                        'closing_position': closing_pos,
                        'volume_ratio': volume_ratio,
                        'continuation': continuation
                    })

            # 判断V型反转：下影线长 + 收盘在上区 + 成交量放大
            lower_shadow = (row['close'] - row['low']) / (row['high'] - row['low']) * 100 if row['high'] > row['low'] else 0
            if lower_shadow > 50 and closing_pos > 60 and volume_ratio > 3.0 and amplitude > 6.0:
                if i < len(df) - 1:
                    next_row = df.iloc[i+1]
                    continuation = next_row['close'] >= row['low']

                    # 避免重复（如果已经作为急拉信号）
                    if not any(s['date'] == row.name.date() and s['type'] == '急拉' for s in signals):
                        signals.append({
                            'date': row.name.date(),
                            'type': 'V型反转',
                            'amplitude': amplitude,
                            'closing_position': closing_pos,
                            'volume_ratio': volume_ratio,
                            'continuation': continuation
                        })

    return signals

